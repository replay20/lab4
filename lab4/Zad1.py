import math

def dzialaniePierwsze(e):
    return (e**4 - math.log(34,2))**(1/3)

print(dzialaniePierwsze(2))

def dzialanieDrugie(f):
    return (math.log(20) + math.cos(45) + f)**(1/3)

print(dzialanieDrugie(3))

def dzialanieTrzecie():
    return (math.log(32,2)+math.sin(45) * (5/8))**(1/4)

print(dzialanieTrzecie())
import math

def dzialaniePierwsze():
    return round((math.e**4 - math.log(34,2))**(1/3),2)

print(dzialaniePierwsze())

def dzialanieDrugie():
    return round((math.log(20) + math.cos(45) + math.e)**(1/3),2)

print(dzialanieDrugie())

def dzialanieTrzecie():
    return round((math.log(32,2)+math.sin(45) * (5/8))**(1/4),2)

print(dzialanieTrzecie())

def dzialanieCzwarte():
    return round(math.log(23,3) + (math.sin(34) + 5) ** (1/3),2)

print(dzialanieCzwarte())

def dzialaniePiate():
    return round((math.log(32,2) + math.pi + math.sin(56)) ** (1/4),2)

print(dzialaniePiate())